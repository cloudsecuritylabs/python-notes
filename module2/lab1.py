# Create a script that collects user input for two variables.
# Then perform the following
# operations on the variables: addition, subtraction, multiplication, and modulo. Print the
# output of each calculation to the console.

first_number = int(input("Please enter the first number: "))
second_number = int(input("Please enter the second number: "))

addition = first_number + second_number
subtraction = first_number - second_number
multiplication = first_number * second_number
modulo = first_number % second_number

print(f'addition={addition}, subtraction={subtraction} , multiplication={multiplication} , modulo={modulo}' )

